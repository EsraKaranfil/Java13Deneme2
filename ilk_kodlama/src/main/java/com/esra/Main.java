package com.esra;

/**
 * public -> Daha sonra ayrıca bir eğitim alınacak. (Erişim belirteci)
 * class -> Daha sonra anlatılacak.
 * Main -> bu oluşturulan dosya (Class Name) adı dır.
 * { } -> süslü parantezler, herhangi bir class, method ya da diğer bileşenlerin kod yazılabilir alanını belirler.
 */
public class Main {

    /**
     * static -> ttanımı yapılan bileşenin adres durumunu uygulama açılırken belirleyen bir
     * belirteç olarak tanımlanabilir. ayrıca ders yapılacak.
     * void [ return type] -> bir method için geri dönüş tipinin olmadığını ifade eder.
     * ayrıca bir dersimiz olacak.
     * main -> çalıştırılabilir bir sınıfın main methodunun olması gereklidir. şimdilik maini kullanacağız.
     * String[] -> bir ifade dizisini anlatır ancak ilk kodlamalarımızda kullanacağımız bir tanım değildir.
     */
    public static void main(String[] args) {

        /**
         * System -> bu bilgisayar.
         * out -> çıktı vermek için export işlemi yapar
         * print -> yazdırma işlemi yapar
         * -ln(line) -> tüm satıra çıktı vermek
         */
        System.out.println("Java13 için ilk kod yazdırma örneği");
    }
}